"""
机器学习数学基础 (1) - 线性代数回顾
====================================
用 numpy 实践 ML 中最核心的线性代数概念。
"""

import numpy as np

# ============================================================
# 1. 向量运算 — ML 中数据的基本单位
# ============================================================
# 在 ML 中，一个样本就是一个向量（如用户特征 [年龄, 收入, 评分]）

print("=" * 60)
print("1. 向量运算")
print("=" * 60)

a = np.array([1, 2, 3])
b = np.array([4, 5, 6])

# 点积（内积）：衡量两个向量的"方向一致性"
# 在 ML 中，线性回归的预测 y = w·x 就是权重向量和特征向量的点积
dot_product = np.dot(a, b)
print(f"向量 a = {a}")
print(f"向量 b = {b}")
print(f"点积 a·b = {dot_product}")  # 1*4 + 2*5 + 3*6 = 32

# 向量范数：衡量向量的"大小"
# L2 范数最常用，正则化（防止过拟合）就是限制权重向量的 L2 范数
l2_norm_a = np.linalg.norm(a)
l1_norm_a = np.linalg.norm(a, ord=1)
print(f"\na 的 L2 范数 = {l2_norm_a:.4f}")  # sqrt(1² + 2² + 3²)
print(f"a 的 L1 范数 = {l1_norm_a:.4f}")    # |1| + |2| + |3|


# ============================================================
# 2. 余弦相似度 — 衡量向量方向的相似程度
# ============================================================
# 在推荐系统、NLP（文档相似度）中广泛使用
# 值域 [-1, 1]：1=完全同向，0=正交，-1=完全反向

print("\n" + "=" * 60)
print("2. 余弦相似度")
print("=" * 60)


def cosine_similarity(v1, v2):
    """计算两个向量的余弦相似度 cos(θ) = (v1·v2) / (||v1|| * ||v2||)"""
    dot_v1v2 = np.dot(v1, v2)
    v1_norm = np.linalg.norm(v1)
    v2_norm = np.linalg.norm(v2)
    tmp = v1_norm * v2_norm
    if tmp == 0:
        return 0
    ret = dot_v1v2 / tmp
    return ret


# 测试：这两个向量方向相近，余弦相似度应接近 1
doc1 = np.array([1, 1, 0, 0])  # "我 喜欢 _ _" 的词向量
doc2 = np.array([1, 1, 1, 0])  # "我 喜欢 猫 _" 的词向量
doc3 = np.array([0, 0, 1, 1])  # "_ _ 猫 狗"   的词向量

sim_12 = cosine_similarity(doc1, doc2)
sim_13 = cosine_similarity(doc1, doc3)
sim_23 = cosine_similarity(doc2, doc3)

print(f"doc1 = {doc1} (我 喜欢)")
print(f"doc2 = {doc2} (我 喜欢 猫)")
print(f"doc3 = {doc3} (猫 狗)")
print(f"\nsim(doc1, doc2) = {sim_12}")  # 应较高
print(f"sim(doc1, doc3) = {sim_13}")    # 应为 0（正交）
print(f"sim(doc2, doc3) = {sim_23}")    # 应较低


# ============================================================
# 3. 矩阵运算 — 数据变换的核心
# ============================================================
# ML 中，数据集 X 是一个 (n_samples, n_features) 矩阵
# 线性变换 Y = XW 是神经网络全连接层的本质

print("\n" + "=" * 60)
print("3. 矩阵运算")
print("=" * 60)

# 模拟：3 个样本，每个有 2 个特征
X = np.array([
    [1, 2],    # 样本1: 特征1=1, 特征2=2
    [3, 4],    # 样本2: 特征1=3, 特征2=4
    [5, 6]     # 样本3: 特征1=5, 特征2=6
])  # X 的形状: (3, 2) → 3行2列

# 权重矩阵：将 2 维特征映射到 3 维输出
W = np.array([
    [0.1, 0.2, 0.3],   # 特征1 对 3 个输出的权重
    [0.4, 0.5, 0.6]    # 特征2 对 3 个输出的权重
])  # W 的形状: (2, 3) → 2行3列

# 矩阵乘法规则：X(3×2) @ W(2×3) → Y(3×3)
# 前提条件：X 的列数(2) == W 的行数(2)，结果形状 = (X的行数, W的列数)
#
# 计算过程：结果 Y[i][j] = X 的第 i 行 与 W 的第 j 列 做点积
#
#   X          @    W                =    Y
#   [1, 2]          [0.1, 0.2, 0.3]      [?, ?, ?]
#   [3, 4]     @    [0.4, 0.5, 0.6]  =   [?, ?, ?]
#   [5, 6]                                [?, ?, ?]
#
#   Y[0][0] = X第0行·W第0列 = 1×0.1 + 2×0.4 = 0.9
#   Y[0][1] = X第0行·W第1列 = 1×0.2 + 2×0.5 = 1.2
#   Y[0][2] = X第0行·W第2列 = 1×0.3 + 2×0.6 = 1.5
#   Y[1][0] = X第1行·W第0列 = 3×0.1 + 4×0.4 = 1.9
#   ...以此类推
#
# 直觉：每个样本（X的一行）通过权重矩阵 W 变换到新的空间
# 神经网络的全连接层就是这个操作：output = input @ weights + bias
Y = X @ W  # 等价于 np.matmul(X, W)
print(f"数据矩阵 X (3×2):\n{X}")
print(f"\n权重矩阵 W (2×3):\n{W}")
print(f"\n输出 Y = X @ W (3×3):\n{Y}")
print(f"→ 每一行是一个样本经过线性变换后的结果")

# 转置：行列互换
print(f"\nX 的转置 X.T (2×3):\n{X.T}")
print(f"→ 转置常用于公式推导，如正规方程 w = (X^T X)^{-1} X^T y")

# 逆矩阵：A @ A_inv = I（单位矩阵）
A = np.array([[2, 1], [5, 3]])
A_inv = np.linalg.inv(A)
print(f"\n矩阵 A:\n{A}")
print(f"A 的逆矩阵:\n{A_inv}")
print(f"验证 A @ A_inv ≈ I:\n{np.round(A @ A_inv, 10)}")


# ============================================================
# 4. 特征值与特征向量 — 理解矩阵的"本质方向"
# ============================================================
# Av = λv  →  矩阵 A 作用于特征向量 v，只改变长度（λ倍），不改变方向
# PCA 降维就是找数据协方差矩阵的特征向量

print("\n" + "=" * 60)
print("4. 特征值与特征向量")
print("=" * 60)

# 协方差矩阵的特征分解 → PCA 的核心
# 假设这是 10 个人的身高(x1)和体重(x2)数据（简化示例）
data = np.array([
    [2.5, 2.4],
    [0.5, 0.7],
    [2.2, 2.9],
    [1.9, 2.2],
    [3.1, 3.0],
    [2.3, 2.7],
    [2.0, 1.6],
    [1.0, 1.1],
    [1.5, 1.6],
    [1.1, 0.9],
])  # 形状 (10, 2)：10 个样本，2 个特征

# 第一步：中心化（减去均值）
# 为什么要中心化？让数据以原点为中心，这样协方差矩阵才能正确反映特征之间的关系
# 例：均值 = [1.81, 1.91]，中心化后第一个样本 [2.5, 2.4] → [0.69, 0.49]
data_centered = data - data.mean(axis=0)

# 第二步：计算协方差矩阵
# 协方差衡量两个特征"一起变化"的程度：
#   cov(x1, x1) = x1 的方差（自身的波动程度）
#   cov(x1, x2) = x1 和 x2 的协方差（正值=同增同减，负值=一增一减）
#
# rowvar=False 表示每一列是一个特征（而非每一行）
# 结果是 2×2 矩阵：
#   [[var(x1),     cov(x1,x2)],
#    [cov(x2,x1),  var(x2)   ]]
cov_matrix = np.cov(data_centered, rowvar=False)
print(f"数据均值: {data.mean(axis=0)}")
print(f"协方差矩阵:\n{cov_matrix}")
# cov(x1,x2) ≈ 0.615 > 0 → 两个特征正相关（身高越高，体重越重）

# 第三步：特征分解
# 对协方差矩阵求特征值和特征向量：cov_matrix × v = λ × v
#
# 特征向量 v：数据方差最大的方向（可以理解为数据"散布"的主轴）
# 特征值 λ：沿该方向的方差大小（越大=该方向上数据越分散=信息量越大）
#
# 直觉：想象一团椭圆形的数据点
#   - 第一个特征向量 = 椭圆的长轴方向（数据最分散）
#   - 第二个特征向量 = 椭圆的短轴方向（数据较集中）
#   - 对应的特征值 = 长轴/短轴的"长度"
eigenvalues, eigenvectors = np.linalg.eig(cov_matrix)
print(f"\n特征值: {eigenvalues}")
print(f"特征向量:\n{eigenvectors}")
# eigenvectors 的每一列是一个特征向量（不是每一行！）
# 第 0 列对应 eigenvalues[0]，第 1 列对应 eigenvalues[1]

print(f"\n→ 最大特征值 {eigenvalues.max():.4f} 对应的特征向量是数据方差最大的方向")
print(f"→ 这就是 PCA 的第一个主成分！")
# PCA 降维：只保留最大的 k 个特征值对应的特征向量
# 例如这里 λ1=1.284 远大于 λ2=0.049，说明 96% 的信息集中在第一个主成分
# 所以可以把 2D 数据投影到 1D，只损失 4% 的信息


# ============================================================
# 5. SVD 分解 — 矩阵分解的"瑞士军刀"
# ============================================================
# SVD 的核心思想：任何数据背后都有一些"隐藏因素"
#
# 比如电影评分数据，表面上是"用户×电影"的数字矩阵
# 但背后其实由少数几个因素决定：
#   因素1：这个人喜欢动作片还是爱情片？
#   因素2：这个人喜欢老片还是新片？
#   ...
#
# SVD 做的事：自动发现这些隐藏因素
# A = U × Σ × Vᵀ
#   U  (用户×因素)：每个用户在各个隐藏因素上的得分
#   Σ  (对角矩阵)：每个隐藏因素的重要程度 ← 这就是奇异值！
#   Vᵀ (因素×电影)：每部电影在各个隐藏因素上的得分
#
# 奇异值越大 = 这个因素越重要，越能解释原始数据
# 应用：降维、去噪、推荐系统（协同过滤）、数据压缩

print("\n" + "=" * 60)
print("5. SVD 奇异值分解")
print("=" * 60)

# 模拟一个用户-电影评分矩阵（0 = 未评分）
#          电影A  电影B  电影C  电影D
# 用户1:  [  5,    4,    0,    1  ]  ← 喜欢电影A/B（可能是动作片）
# 用户2:  [  4,    0,    0,    1  ]  ← 也喜欢电影A
# 用户3:  [  1,    1,    0,    5  ]  ← 喜欢电影D（可能是爱情片）
# 用户4:  [  0,    1,    5,    4  ]  ← 喜欢电影C/D
# 用户5:  [  0,    0,    4,    0  ]  ← 只看了电影C
ratings = np.array([
    [5, 4, 0, 1],
    [4, 0, 0, 1],
    [1, 1, 0, 5],
    [0, 1, 5, 4],
    [0, 0, 4, 0],
])
print(f"用户-电影评分矩阵 (5用户 × 4电影):\n{ratings}")

# SVD 分解
# full_matrices=False：只返回有效部分（紧凑形式），不补零
U, sigma, Vt = np.linalg.svd(ratings, full_matrices=False)
print(f"\n奇异值 σ: {np.round(sigma, 2)}")
# σ = [8.66, 6.77, 4.25, 2.26]
#       ↑      ↑      ↑      ↑
#     因素1  因素2  因素3  因素4
#    最重要                不重要（可能是噪声）
#
# 奇异值的"能量"占比可以看每个因素解释了多少信息：
# 因素1 的能量占比 = 8.66² / (8.66² + 6.77² + 4.25² + 2.26²) ≈ 52%
# 前2个因素的能量占比 ≈ 84% → 只用2个因素就能解释84%的评分数据！
print(f"→ 奇异值从大到小排列，前几个包含了矩阵的主要信息")

# 降维：只保留前 k 个最重要的因素来近似重建原始矩阵
# 就像描述一个人：
#   "他喜欢动作片" → 只用1个因素，粗略但有用
#   "他喜欢动作片，而且偏爱成龙" → 2个因素，更精确
#   保留全部因素 → 完美还原，但太复杂
k = 2
# U[:, :k]          → 只取前2个因素的用户得分 (5×2)
# np.diag(sigma[:k]) → 只取前2个奇异值构成的对角矩阵 (2×2)
# Vt[:k, :]          → 只取前2个因素的电影得分 (2×4)
# 矩阵乘法：(5×2) @ (2×2) @ (2×4) = (5×4) → 和原始矩阵同形状
ratings_approx = U[:, :k] @ np.diag(sigma[:k]) @ Vt[:k, :]
print(f"\n用 rank-{k} 近似重建的矩阵:\n{np.round(ratings_approx, 1)}")
print(f"→ 原始矩阵中的 0（未评分）现在有了预测值，这就是推荐系统的基本原理！")
# 比如用户5只看了电影C(评分4)，重建后电影D也有了预测分
# → 系统可以推荐电影D给用户5

# 重建误差：衡量丢掉的因素造成了多少信息损失
error = np.linalg.norm(ratings - ratings_approx)
print(f"\n重建误差 (Frobenius 范数): {error:.4f}")


print("\n" + "=" * 60)
print("总结")
print("=" * 60)
