import numpy as np
import matplotlib
matplotlib.use('Agg')  # 无 GUI 环境下保存图片
import matplotlib.pyplot as plt


# ============================================================
# 1. 导数的直觉 — "变化率"
# ============================================================
# 导数 = 函数在某一点的变化速度
# f'(x) = lim(h→0) [f(x+h) - f(x)] / h
#
# 直觉：你在爬山，导数告诉你"脚下的坡有多陡"
#   f'(x) > 0 → 上坡（函数在增大）
#   f'(x) < 0 → 下坡（函数在减小）
#   f'(x) = 0 → 山顶或山谷（极值点）

print("=" * 60)
print("1. 导数 — 函数的变化率")
print("=" * 60)

# 例子：f(x) = x²
# f'(x) = 2x（导数公式）
# 在 x=3 处：f'(3) = 6 → 函数在快速增大
# 在 x=0 处：f'(0) = 0 → 这是最低点！
# 在 x=-2 处：f'(-2) = -4 → 函数在减小

def f(x):
    """简单的二次函数 f(x) = x²"""
    return x ** 2

def f_derivative(x):
    """f(x) = x² 的导数 f'(x) = 2x"""
    return 2 * x

# 用数值方法验证导数（有限差分法）
# f'(x) ≈ [f(x+h) - f(x)] / h，当 h 很小时
x = 3.0
h = 0.0001
numerical_derivative = (f(x + h) - f(x)) / h
analytical_derivative = f_derivative(x)

print(f"f(x) = x²")
print(f"在 x={x} 处：")
print(f"  数值导数（有限差分）= {numerical_derivative:.6f}")
print(f"  解析导数（公式 2x）= {analytical_derivative:.6f}")
print(f"  两者几乎相等，验证了导数公式的正确性")


# ============================================================
# 2. 偏导数 — 多变量函数的变化率
# ============================================================
# ML 中的损失函数通常有多个参数：L(w1, w2, ..., wn)
# 偏导数 = 固定其他变量，只看一个变量变化时函数怎么变
#
# 例子：f(x, y) = x² + y²
#   ∂f/∂x = 2x（把 y 当常数，对 x 求导）
#   ∂f/∂y = 2y（把 x 当常数，对 y 求导）
#
# 直觉：你站在一个碗形山谷里
#   ∂f/∂x 告诉你"往东走会上坡还是下坡"
#   ∂f/∂y 告诉你"往北走会上坡还是下坡"

print("\n" + "=" * 60)
print("2. 偏导数 — 多变量的变化率")
print("=" * 60)

def f_2d(x, y):
    """二元函数 f(x,y) = x² + y²（碗形曲面，最低点在原点）"""
    return x**2 + y**2

def f_2d_df_dx(x):
    return x * 2

def f_2d_df_dy(y):
    return y * 2

# 在点 (3, 4) 处计算偏导数
x, y = 3.0, 4.0
h = 0.0001

# 对 x 的偏导数：固定 y=4，只变化 x
df_dx = (f_2d(x + h, y) - f_2d(x, y)) / h  # 数值法
print(f"f(x, y) = x² + y²")
print(f"在点 ({x}, {y}) 处：")
print(f"  ∂f/∂x = {df_dx:.4f}（解析值 = 2×{x} = {2*x}）")

# 对 y 的偏导数：固定 x=3，只变化 y
df_dy = (f_2d(x, y + h) - f_2d(x, y)) / h  # 数值法
print(f"  ∂f/∂y = {df_dy:.4f}（解析值 = 2×{y} = {2*y}）")


# ============================================================
# 3. 梯度 — 所有偏导数组成的向量
# ============================================================
# 梯度 ∇f = [∂f/∂x, ∂f/∂y, ...]
#
# 关键性质：梯度指向函数增长最快的方向
# 所以 -梯度（负梯度）指向函数下降最快的方向
#
# 直觉：你蒙着眼站在碗形山谷里，想走到最低点
#   梯度告诉你"最陡的上坡方向"
#   负梯度告诉你"最陡的下坡方向" → 沿着它走就能到谷底！

print("\n" + "=" * 60)
print("3. 梯度 — 最陡方向")
print("=" * 60)

# 在点 (3, 4) 处的梯度
gradient = np.array([2 * x, 2 * y])  # ∇f = [2x, 2y] = [6, 8]
print(f"在点 ({x}, {y}) 处的梯度 ∇f = {gradient}")
print(f"梯度方向指向函数增长最快的方向（远离谷底）")
print(f"负梯度方向 = {-gradient} → 指向函数下降最快的方向（朝向谷底）")


# ============================================================
# 4. 梯度下降 — ML 模型"学习"的核心算法
# ============================================================
# 算法非常简单：
#   重复以下步骤直到收敛：
#     参数 = 参数 - 学习率 × 梯度
#     即 w = w - lr × ∇L(w)
#
# 学习率（lr）控制每一步走多远：
#   太大 → 步子太大，可能跨过最低点，来回震荡
#   太小 → 步子太小，收敛很慢
#   合适 → 稳步走到最低点
#
# 这就是 ML 中"训练"的本质：
#   1. 用当前参数计算损失（模型多"差"）
#   2. 计算损失对参数的梯度（参数该往哪调）
#   3. 沿负梯度方向更新参数（让模型变"好"一点）
#   4. 重复，直到损失不再明显减小

print("\n" + "=" * 60)
print("4. 梯度下降 — 找到函数的最低点")
print("=" * 60)


def gradient_descent_1d(start_x, learning_rate, n_steps):
    """
    一维梯度下降：最小化 f(x) = x²

    参数：
        start_x: 起始位置
        learning_rate: 学习率（每步走多远）
        n_steps: 迭代次数
    返回：
        history: 每一步的 (x, f(x)) 记录
    """
    x = start_x
    history = [(x, f(x))]

    for i in range(n_steps):
        grad = f_derivative(x)       # 第1步：计算梯度 f'(x) = 2x
        x = x - learning_rate * grad  # 第2步：沿负梯度方向走一步
        history.append((x, f(x)))

    return history


# 演示不同学习率的效果
print("\n--- 学习率 = 0.1（合适）---")
history = gradient_descent_1d(start_x=5.0, learning_rate=0.1, n_steps=20)
for i in [0, 1, 2, 5, 10, 20]:
    x_val, f_val = history[i]
    print(f"  第{i:2d}步: x = {x_val:8.4f}, f(x) = {f_val:8.4f}")

print("\n--- 学习率 = 0.01（太小）---")
history_slow = gradient_descent_1d(start_x=5.0, learning_rate=0.01, n_steps=20)
for i in [0, 1, 2, 5, 10, 20]:
    x_val, f_val = history_slow[i]
    print(f"  第{i:2d}步: x = {x_val:8.4f}, f(x) = {f_val:8.4f}")

print("\n--- 学习率 = 0.9（太大）---")
history_fast = gradient_descent_1d(start_x=5.0, learning_rate=0.9, n_steps=20)
for i in [0, 1, 2, 5, 10, 20]:
    x_val, f_val = history_fast[i]
    print(f"  第{i:2d}步: x = {x_val:8.4f}, f(x) = {f_val:8.4f}")

print("\n→ 观察：lr=0.1 稳步收敛，lr=0.01 太慢，lr=0.9 来回震荡")


# ============================================================
# 5. 二维梯度下降 — 更接近真实 ML 场景
# ============================================================
# 真实 ML 模型有成百上千个参数，这里用 2 个参数演示
# 最小化 f(x, y) = x² + y²（碗形曲面，最低点在原点）

print("\n" + "=" * 60)
print("5. 二维梯度下降")
print("=" * 60)


def gradient_descent_2d(start, learning_rate, n_steps):
    """
    二维梯度下降：最小化 f(x, y) = x² + y²
    分别对 x 和 y 计算偏导数并更新
    """
    x = start[0]
    y = start[1]
    history = [np.array([x, y])]

    for i in range(n_steps):
        x = x - learning_rate * f_2d_df_dx(x)
        y = y - learning_rate * f_2d_df_dy(y)
        history.append(np.array([x, y]))

    return history


# 测试
start = np.array([4.0, 3.0])
history_2d = gradient_descent_2d(start, learning_rate=0.1, n_steps=30)

if history_2d is not None:
    print(f"起始点: {history_2d[0]}, f = {f_2d(*history_2d[0]):.4f}")
    print(f"第5步:  {np.round(history_2d[5], 4)}, f = {f_2d(*history_2d[5]):.4f}")
    print(f"第10步: {np.round(history_2d[10], 4)}, f = {f_2d(*history_2d[10]):.4f}")
    print(f"第30步: {np.round(history_2d[30], 4)}, f = {f_2d(*history_2d[30]):.4f}")
    print(f"\n→ 从 (4,3) 出发，逐步逼近最低点 (0,0)")


# ============================================================
# 6. 链式法则与反向传播
# ============================================================
#
# 【什么是反向传播？】
# 一句话：从最终结果倒推回去，算出"每个参数对误差贡献了多少"
#
# 【为什么需要它？】
# 模型训练时，我们想知道：调整某个参数，损失会怎么变？
# 问题是：参数到损失之间隔了好几步计算
# 所以我们需要链式法则，把影响一步步传递回来
#
# 【链式法则是什么？】
# 如果 A 影响 B，B 影响 C，那么：
#   A 对 C 的影响 = A 对 B 的影响 × B 对 C 的影响
# 就像多米诺骨牌：第一张倒了 → 第二张倒了 → 第三张倒了
# 第一张对第三张的影响 = 每一环的影响相乘

print("\n" + "=" * 60)
print("6. 链式法则与反向传播")
print("=" * 60)

# ----------------------------------------------------------------
# 用一个最简单的神经网络来理解反向传播
# 只有 1 个输入、1 个参数、1 个偏置，3 步计算
# ----------------------------------------------------------------

x = 2.0      # 输入（比如用户年龄）
w = 3.0      # 参数（需要学习的权重）
b = 1.0      # 偏置（需要学习的偏置）
target = 10.0  # 目标值（真实标签）

print(f"输入 x={x}, 参数 w={w}, 偏置 b={b}, 目标={target}")

# ===== 第一阶段：前向传播（从左到右算结果）=====
# 就是把数据从输入一步步算到输出

a = x * w        # 第1步：乘法 → a = 2 × 3 = 6
z = a + b        # 第2步：加法 → z = 6 + 1 = 7
L = (z - target) ** 2  # 第3步：算损失 → L = (7-10)² = 9

print(f"\n【前向传播】从左到右计算：")
print(f"  第1步 乘法: a = x × w = {x} × {w} = {a}")
print(f"  第2步 加法: z = a + b = {a} + {b} = {z}")
print(f"  第3步 损失: L = (z - target)² = ({z} - {target})² = {L}")
print(f"  当前损失 L = {L}，我们希望 L 变小 → 需要调整 w 和 b")

# ===== 第二阶段：反向传播（从右到左算梯度）=====
# 从损失 L 出发，倒推每个参数"贡献了多少误差"
#
# 计算图（想象一条链子）：
#   x, w → [乘法] → a → [加法] → z → [平方误差] → L
#                    b ↗
#
# 反向传播就是从 L 沿着这条链子往回走

print(f"\n【反向传播】从右到左，追问'谁的锅'：")

# 第3步反向：L = (z - target)²
# 问：z 变化 1，L 变化多少？
# 答：dL/dz = 2 × (z - target) = 2 × (7 - 10) = -6
# 含义：z 每增大 1，L 会减小 6 → z 应该变大！
dL_dz = 2 * (z - target)
print(f"\n  第3步反向: L = (z-target)²")
print(f"    dL/dz = 2×(z-target) = 2×({z}-{target}) = {dL_dz}")
print(f"    含义: z 每增大1, L 变化 {dL_dz}（负数=L会减小=好事）")

# 第2步反向：z = a + b
# 问：a 变化 1，z 变化多少？ → dz/da = 1（加法直接传递）
# 问：b 变化 1，z 变化多少？ → dz/db = 1
# 链式法则串起来：
#   dL/da = dL/dz × dz/da = -6 × 1 = -6
#   dL/db = dL/dz × dz/db = -6 × 1 = -6
dz_da = 1
dz_db = 1
dL_da = dL_dz * dz_da
dL_db = dL_dz * dz_db
print(f"\n  第2步反向: z = a + b")
print(f"    dz/da = {dz_da}, dz/db = {dz_db}（加法：变化量直接传递）")
print(f"    链式法则: dL/da = dL/dz × dz/da = {dL_dz} × {dz_da} = {dL_da}")
print(f"    链式法则: dL/db = dL/dz × dz/db = {dL_dz} × {dz_db} = {dL_db}")

# 第1步反向：a = x * w
# 问：w 变化 1，a 变化多少？ → da/dw = x = 2
# 链式法则串起来：
#   dL/dw = dL/da × da/dw = -6 × 2 = -12
# 这就是我们最终要的答案！w 的梯度 = -12
da_dw = x
dL_dw = dL_da * da_dw
print(f"\n  第1步反向: a = x × w")
print(f"    da/dw = x = {da_dw}（乘法：另一个乘数就是导数）")
print(f"    链式法则: dL/dw = dL/da × da/dw = {dL_da} × {da_dw} = {dL_dw}")
print(f"    ★ 这就是 w 的梯度！含义: w 每增大1, L 变化 {dL_dw}")

# ===== 第三阶段：更新参数 =====
lr = 0.1
w_new = w - lr * dL_dw
b_new = b - lr * dL_db
print(f"\n【更新参数】参数 = 参数 - 学习率 × 梯度")
print(f"  w_new = {w} - {lr} × ({dL_dw}) = {w_new}")
print(f"  b_new = {b} - {lr} × ({dL_db}) = {b_new}")

# 验证：新参数确实让损失变小了
z_new = x * w_new + b_new
L_new = (z_new - target) ** 2
print(f"\n【验证】用新参数算损失：")
print(f"  z_new = {x} × {w_new} + {b_new} = {z_new}")
print(f"  L_new = ({z_new} - {target})² = {L_new:.2f}")
print(f"  损失从 {L} → {L_new:.2f}，减小了！✓")

# 数值验证梯度的正确性
h = 0.0001
L_plus = ((x * (w + h) + b) - target) ** 2
numerical_dL_dw = (L_plus - L) / h
print(f"\n【数值验证】")
print(f"  反向传播算的 dL/dw = {dL_dw}")
print(f"  数值法算的   dL/dw ≈ {numerical_dL_dw:.4f}")
print(f"  两者一致 ✓")

print(f"""
【总结：反向传播的完整过程】

  ┌──────┐     ┌──────┐     ┌──────┐     ┌──────┐
  │x={x}   │ ──→ │a=x×w │ ──→ │z=a+b │ ──→ │L=(z-t)²│
  │w={w}   │     │ ={a}  │     │ ={z}  │     │ ={L}   │
  └──────┘     └──────┘     └──────┘     └──────┘
                                            │
  前向：从左到右算结果                        │ 损失 L={L}
                                            ↓
  ┌──────┐     ┌──────┐     ┌──────┐     ┌──────┐
  │dL/dw  │ ←── │dL/da  │ ←── │dL/dz  │ ←── │dL/dL  │
  │ ={dL_dw} │     │ ={dL_da}  │     │ ={dL_dz}  │     │ =1    │
  └──────┘     └──────┘     └──────┘     └──────┘

  反向：从右到左，每一步都是"上一步传来的梯度 × 本步的局部导数"
  dL/dw = dL/dz × dz/da × da/dw = {dL_dz} × {dz_da} × {da_dw} = {dL_dw}

  PyTorch 中：
    loss.backward()   →  自动做上面的反向传播，算出所有参数的梯度
    optimizer.step()  →  用梯度更新所有参数
""")


